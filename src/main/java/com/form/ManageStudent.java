    private void importFromFile(File selectedFile) {
        String csvFile = selectedFile.toString();
        String cvsSplitBy = ",";
        BufferedReader br;
        try {
            br = new BufferedReader(new FileReader(csvFile));
            String line = "";
            int d = 0;
            String mssvnoinsert = "";
            ClassDAO classDAO = new ClassDAO();
            StudentDAO studentDAO = new StudentDAO();
            UserDAO userDAO = new UserDAO();
            try {
                if ((line = br.readLine()) != null) {
                    String[] l = line.split(cvsSplitBy);
                    ClassEntity new_class = new ClassEntity(l[0], l[1]);
                    String classid= new_class.getClassId();
                    if (classDAO.create(new_class) == true) {
                        while ((line = br.readLine()) != null) {
                            String[] sv = line.split(cvsSplitBy);
                            StudentEntity new_student = new StudentEntity(sv[0],classid, sv[1], sv[2], sv[3]);
                            UserEntity new_user = new UserEntity(sv[0], sv[0], 1, "Sinh Vien");
                            if (StudentDAO.create(new_student) == true && UserDAO.create(new_user) == true) {
                                d++;
                            }
                        }
                    }
                }
                JOptionPane.showMessageDialog(this, "Da insert " + d + " Sinh vien ");
            } catch (IOException ex) {
                Logger.getLogger(ManageStudent.class.getName()).log(Level.SEVERE, null, ex);
            }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(ManageStudent.class.getName()).log(Level.SEVERE, null, ex);
        }
        initLayout();
    }


