    private void listClass(String class_id) {
        int stt = 1;
        List<StudentEntity> dssv = StudentDAO.getListStudent(class_id);
        DefaultTableModel tableModel = new DefaultTableModel();
        tableModel.setColumnIdentifiers(columnNames);
        for (StudentEntity sv : dssv) {
            String[] rows = new String[5];
            rows[0] = String.valueOf(stt);
            rows[1] = sv.getStudentId();
            rows[2] = sv.getFullName();
            rows[3] = sv.getGender();
            rows[4] = sv.getIdcard();
            tableModel.addRow(rows);
            stt++;
        }
        jTable1.setModel(tableModel);
    }
